// ===== Track Switcher =====
function initTrackSwitcher() {
  const switcher = document.getElementById('trackSwitcher');
  if (!switcher) return;
  
  // Set initial state
  switcher.querySelectorAll('.track-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.track === currentTrack);
    btn.addEventListener('click', () => {
      if (btn.dataset.track === currentTrack) return;
      currentTrack = btn.dataset.track;
      localStorage.setItem('track', currentTrack);
      switcher.querySelectorAll('.track-btn').forEach(b => b.classList.toggle('active', b === btn));
      buildNavigation();
      // Load first doc in new track
      const firstSection = NAV_SECTIONS.find(s => s.track === currentTrack || s.track === 'shared');
      if (firstSection && firstSection.items.length) {
        loadDocument(firstSection.items[0].file);
      }
    });
  });
}

// ===== Build Sidebar Navigation =====
function buildNavigation() {
  const navContainer = document.getElementById('navContainer');
  if (!navContainer || typeof NAV_SECTIONS === 'undefined') return;

  let html = '';
  NAV_SECTIONS.filter(s => s.track === currentTrack || s.track === 'shared').forEach(section => {
    html += `<div class="nav-section"><h3>${section.title}</h3>`;
    section.items.forEach(item => {
      html += `<a class="nav-item" data-file="${item.file}" href="#">`;
      html += `<span class="nav-text">${item.title}</span>`;
      html += `</a>`;
    });
    html += `</div>`;
  });

  navContainer.innerHTML = html;

  // 绑定点击事件
  navContainer.querySelectorAll('.nav-item').forEach(item => {
    item.addEventListener('click', (e) => {
      e.preventDefault();
      const file = item.dataset.file;
      if (file) loadDocument(file);
    });
  });
}

// ===== Markdown Rendering =====
marked.setOptions({
  highlight: function (code, lang) {
    if (lang && hljs.getLanguage(lang)) {
      return hljs.highlight(code, { language: lang }).value;
    }
    return hljs.highlightAuto(code).value;
  },
  breaks: false,
  gfm: true,
});

// Custom renderer for code blocks with language labels
const renderer = new marked.Renderer();
const originalCodeRenderer = renderer.code;

renderer.code = function(code, language, escaped) {
  const lang = (language || '').trim();
  let highlighted;
  if (lang && hljs.getLanguage(lang)) {
    highlighted = hljs.highlight(code, { language: lang }).value;
  } else {
    highlighted = hljs.highlightAuto(code).value;
  }
  const langAttr = lang ? ` data-lang="${lang}"` : '';
  return `<pre${langAttr}><code class="hljs language-${lang}">${highlighted}</code></pre>`;
};

// Handle checkboxes in markdown
renderer.listitem = function(text) {
  if (text.startsWith('<input')) {
    return `<li style="list-style:none;margin-left:-20px">${text}</li>\n`;
  }
  return `<li>${text}</li>\n`;
};

marked.setOptions({ renderer });

// ===== Load Document =====
function loadDocument(filename, searchQuery) {
  if (currentFile === filename && !searchQuery) return;
  currentFile = filename;

  // Update nav active state
  document.querySelectorAll('.nav-item').forEach(item => {
    item.classList.toggle('active', item.dataset.file === filename);
  });

  // Update title
  docTitle.textContent = DOC_TITLES[filename] || filename;

  // Load from bundled DOCS object (generated by build.py)
  if (typeof DOCS === 'undefined') {
    article.innerHTML = `
      <div style="text-align:center;padding:60px 0;color:var(--text-secondary)">
        <p style="font-size:48px;margin-bottom:16px">📦</p>
        <p>请先运行打包脚本生成 docs.js</p>
        <pre style="text-align:left;max-width:400px;margin:20px auto;padding:16px;background:var(--code-bg);border-radius:8px;font-size:13px">python build.py</pre>
        <p style="font-size:13px;margin-top:8px">或直接双击 <b>启动网站.bat</b></p>
      </div>`;
    return;
  }

  const text = DOCS[filename];
  if (!text) {
    article.innerHTML = `
      <div style="text-align:center;padding:60px 0;color:var(--text-secondary)">
        <p style="font-size:48px;margin-bottom:16px">📄</p>
        <p>文档未找到: ${filename}</p>
        <p style="font-size:13px;margin-top:8px">请重新运行 <b>python build.py</b> 打包</p>
      </div>`;
    return;
  }

  // Render markdown
  article.innerHTML = marked.parse(text);

  // Build table of contents
  buildTOC();

  // Highlight and scroll to search match
  if (searchQuery) {
    highlightAndScroll(searchQuery);
  } else {
    // Scroll to top
    contentBody.scrollTop = 0;
    window.scrollTo(0, 0);
  }

  // Save current doc to URL hash
  window.location.hash = encodeURIComponent(filename);

  // Close mobile sidebar
  sidebar.classList.remove('open');
}

// ===== Table of Contents =====
function buildTOC() {
  const headings = article.querySelectorAll('h2, h3');
  let tocHTML = '';

  headings.forEach((h, i) => {
    const id = `heading-${i}`;
    h.id = id;
    const level = h.tagName.toLowerCase();
    const cls = level === 'h3' ? ' toc-h3' : '';
    tocHTML += `<a href="#${id}" class="${cls}" data-id="${id}">${h.textContent}</a>`;
  });

  tocNav.innerHTML = tocHTML;

  // TOC click handler
  tocNav.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      const target = document.getElementById(link.dataset.id);
      if (target) {
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}

