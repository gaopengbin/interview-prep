const searchShortcut = document.getElementById('searchShortcut');

let searchIndex = null; // cached search index
let selectedResultIdx = -1;

// Build search index from DOCS
function buildSearchIndex() {
  if (searchIndex || typeof DOCS === 'undefined') return;
  searchIndex = [];
  for (const [filename, content] of Object.entries(DOCS)) {
    const title = DOC_TITLES[filename] || filename;
    // Find which section this doc belongs to
    let sectionName = '';
    if (typeof NAV_SECTIONS !== 'undefined') {
      for (const sec of NAV_SECTIONS) {
        if (sec.items.some(it => it.file === filename)) {
          sectionName = sec.title;
          break;
        }
      }
    }
    searchIndex.push({ filename, title, sectionName, content: content.toLowerCase(), rawContent: content });
  }
}

// Perform search
function performSearch(query) {
  if (!query || query.length < 2) {
    searchResults.innerHTML = `<div class="search-hint">输入至少 2 个字符开始搜索</div>`;
    searchOverlay.classList.remove('active');
    selectedResultIdx = -1;
    return;
  }

  buildSearchIndex();
  if (!searchIndex) return;

  const q = query.toLowerCase();
  const results = [];

  for (const item of searchIndex) {
    // Search in title and content
    const titleMatch = item.title.toLowerCase().includes(q);
    const contentIdx = item.content.indexOf(q);

    if (titleMatch || contentIdx !== -1) {
      // Extract snippet around match
      let snippet = '';
      if (contentIdx !== -1) {
        const start = Math.max(0, contentIdx - 40);
        const end = Math.min(item.rawContent.length, contentIdx + query.length + 80);
        const raw = item.rawContent.slice(start, end);
        // Remove markdown syntax for cleaner display
        snippet = raw.replace(/[#*`\[\]()_~>|]/g, '').replace(/\n/g, ' ').trim();
        if (start > 0) snippet = '…' + snippet;
        if (end < item.rawContent.length) snippet += '…';
      } else {
        // Title-only match, show first 120 chars of content
        snippet = item.rawContent.slice(0, 120).replace(/[#*`\[\]()_~>|]/g, '').replace(/\n/g, ' ').trim() + '…';
      }

      // Count total matches
      let count = 0;
      let pos = 0;
      while ((pos = item.content.indexOf(q, pos)) !== -1) {
        count++;
        pos += q.length;
      }

      results.push({
        filename: item.filename,
        title: item.title,
        section: item.sectionName,
        snippet,
        count,
        titleMatch
      });
    }
  }

  // Sort: title matches first, then by match count
  results.sort((a, b) => {
    if (a.titleMatch !== b.titleMatch) return b.titleMatch - a.titleMatch;
    return b.count - a.count;
  });

  renderSearchResults(results, query);
}

// Render results
function renderSearchResults(results, query) {
  selectedResultIdx = -1;

  if (results.length === 0) {
    searchResults.innerHTML = `
      <div class="search-empty">
        <div class="icon">🔍</div>
        <div>没有找到匹配「${escapeHTML(query)}」的结果</div>
      </div>`;
    searchOverlay.classList.add('active');
    return;
  }

  const escapedQuery = escapeHTML(query);
  const re = new RegExp(`(${escapeRegex(query)})`, 'gi');

  let html = `<div class="search-count">找到 ${results.length} 篇文档</div>`;
  results.forEach((r, i) => {
    const highlightedTitle = r.title.replace(re, '<mark>$1</mark>');
    const highlightedSnippet = r.snippet.replace(re, '<mark>$1</mark>');
    html += `
      <div class="search-result-item" data-file="${r.filename}" data-idx="${i}">
        <div class="search-result-title">${highlightedTitle}</div>
        ${r.section ? `<div class="search-result-section">${r.section} · ${r.count} 处匹配</div>` : ''}
        <div class="search-result-snippet">${highlightedSnippet}</div>
      </div>`;
  });

  searchResults.innerHTML = html;
  searchOverlay.classList.add('active');

  // Bind click on results
  searchResults.querySelectorAll('.search-result-item').forEach(el => {
    el.addEventListener('click', () => {
      const file = el.dataset.file;
      const q = searchInput.value.trim();
      closeSearch();
      loadDocument(file, q);
    });
  });
}

// Open / close search
function openSearch() {
  searchInput.focus();
  if (searchInput.value.length >= 2) {
    searchOverlay.classList.add('active');
  }
}

function closeSearch() {
  searchOverlay.classList.remove('active');
  searchInput.blur();
  selectedResultIdx = -1;
}

// Debounce helper
function debounce(fn, delay) {
  let timer;
  return function (...args) {
    clearTimeout(timer);
    timer = setTimeout(() => fn.apply(this, args), delay);
  };
}

// Escape HTML
function escapeHTML(str) {
  return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

// Escape regex special chars
function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// Highlight search query in article and scroll to first match
function highlightAndScroll(query) {
  if (!query) return;

  // Remove previous search highlights
  article.querySelectorAll('mark.search-hl').forEach(m => {
    const parent = m.parentNode;
    parent.replaceChild(document.createTextNode(m.textContent), m);
    parent.normalize();
  });

  const re = new RegExp(escapeRegex(query), 'gi');
  const walker = document.createTreeWalker(article, NodeFilter.SHOW_TEXT, null);
  const matches = [];

  // Collect all text nodes that contain the query
  while (walker.nextNode()) {
    const node = walker.currentNode;
    if (re.test(node.nodeValue)) {
      matches.push(node);
      re.lastIndex = 0; // reset regex
    }
  }

  if (matches.length === 0) {
    contentBody.scrollTop = 0;
    window.scrollTo(0, 0);
    return;
  }

  // Highlight all matches (wrap in <mark>)
  let firstMark = null;
  matches.forEach(textNode => {
    const fragment = document.createDocumentFragment();
    let lastIdx = 0;
    const text = textNode.nodeValue;
    let match;
    re.lastIndex = 0;
    while ((match = re.exec(text)) !== null) {
      // Text before match
      if (match.index > lastIdx) {
        fragment.appendChild(document.createTextNode(text.slice(lastIdx, match.index)));
      }
      // Highlighted match
      const mark = document.createElement('mark');
      mark.className = 'search-hl';
      mark.textContent = match[0];
      fragment.appendChild(mark);
      if (!firstMark) firstMark = mark;
      lastIdx = re.lastIndex;
    }
    // Remaining text
    if (lastIdx < text.length) {
      fragment.appendChild(document.createTextNode(text.slice(lastIdx)));
    }
    textNode.parentNode.replaceChild(fragment, textNode);
  });

  // Scroll to first match
  if (firstMark) {
    setTimeout(() => {
      firstMark.scrollIntoView({ behavior: 'smooth', block: 'center' });
    }, 50);
  }
}

// Navigate results with arrow keys
function navigateResults(direction) {
  const items = searchResults.querySelectorAll('.search-result-item');
  if (items.length === 0) return;

  items.forEach(el => el.classList.remove('selected'));

  if (direction === 'down') {
    selectedResultIdx = (selectedResultIdx + 1) % items.length;
  } else {
    selectedResultIdx = selectedResultIdx <= 0 ? items.length - 1 : selectedResultIdx - 1;
  }

  items[selectedResultIdx].classList.add('selected');
  items[selectedResultIdx].scrollIntoView({ block: 'nearest' });
}

