// ===== Keyboard Shortcuts =====
document.addEventListener('keydown', (e) => {
  // Ctrl+K to focus search
  if ((e.ctrlKey || e.metaKey) && e.key === 'k') {
    e.preventDefault();
    openSearch();
    return;
  }

  // When search is focused
  if (document.activeElement === searchInput) {
    if (e.key === 'ArrowDown') {
      e.preventDefault();
      navigateResults('down');
    } else if (e.key === 'ArrowUp') {
      e.preventDefault();
      navigateResults('up');
    } else if (e.key === 'Enter') {
      e.preventDefault();
      const items = searchResults.querySelectorAll('.search-result-item');
      const q = searchInput.value.trim();
      if (selectedResultIdx >= 0 && items[selectedResultIdx]) {
        const file = items[selectedResultIdx].dataset.file;
        closeSearch();
        loadDocument(file, q);
      } else if (items.length > 0) {
        const file = items[0].dataset.file;
        closeSearch();
        loadDocument(file, q);
      }
    } else if (e.key === 'Escape') {
      closeSearch();
    }
    return;
  }

  // Escape closes sidebar on mobile
  if (e.key === 'Escape') {
    sidebar.classList.remove('open');
  }
});

// ===== Initialize =====
function init() {
  initTrackSwitcher();
  
  // Check if hash belongs to a different track
  const hash = decodeURIComponent(window.location.hash.slice(1));
  if (hash && DOC_TITLES[hash]) {
    // Find which track this doc belongs to
    const docSection = NAV_SECTIONS.find(s => s.items.some(i => i.file === hash));
    if (docSection && docSection.track !== 'shared' && docSection.track !== currentTrack) {
      currentTrack = docSection.track;
      localStorage.setItem('track', currentTrack);
      document.querySelectorAll('.track-btn').forEach(b => {
        b.classList.toggle('active', b.dataset.track === currentTrack);
      });
    }
  }
  
  buildNavigation();

  const defaultFile = (typeof NAV_SECTIONS !== 'undefined' && NAV_SECTIONS.length > 0)
    ? NAV_SECTIONS.filter(s => s.track === currentTrack || s.track === 'shared')[0]?.items[0]?.file
    : null;

  if (hash && DOC_TITLES[hash]) {
    loadDocument(hash);
  } else if (defaultFile) {
    loadDocument(defaultFile);
  }
}

// Handle hash change
window.addEventListener('hashchange', () => {
  const hash = decodeURIComponent(window.location.hash.slice(1));
  if (hash && DOC_TITLES[hash]) {
    loadDocument(hash);
  }
});

init();
